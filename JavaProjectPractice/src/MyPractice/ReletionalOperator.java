package MyPractice;

import java.util.Scanner;

public class ReletionalOperator {
	public static void main(String[] args) {
		int a;
		int b;
		System.out.println("Enter any two number");
		Scanner r=new Scanner(System.in);
		a=r.nextInt();
		b=r.nextInt();
		System.out.println(a==b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a!=b);


	}

}
